#ifndef EGG_H
#define EGG_H

#include <ngl/Vec3.h>
#include <ngl/Transformation.h>


#include <ngl/Camera.h>
#include <ngl/ShaderLib.h>

class Egg
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor
    /// @param pos; position of sphere
    /// @param rad; radius of sphere
    //----------------------------------------------------------------------------------------------------------------------
    Egg(const ngl::Vec3 &_pos, GLfloat _rad);

    //----------------------------------------------------------------------------------------------------------------------
    ///@brief Return position or radius of egg
    //----------------------------------------------------------------------------------------------------------------------
    inline ngl::Vec3 getPos() const {return m_pos;}
    inline GLfloat getRadius() const {return m_radius;}

    //----------------------------------------------------------------------------------------------------------------------
    ///@brief Set radius and position of egg
    /// @param _pos; position to set
    /// @param _rad; radius to set
    //----------------------------------------------------------------------------------------------------------------------
    void set(const ngl::Vec3 &_pos, GLfloat _rad);

private:

    //----------------------------------------------------------------------------------------------------------------------
    ///@brief position of sphere
    //----------------------------------------------------------------------------------------------------------------------
    ngl::Vec3 m_pos;

    //----------------------------------------------------------------------------------------------------------------------
    ///@brief radius of sphere
    //----------------------------------------------------------------------------------------------------------------------
    GLfloat m_radius;
};

#endif // EGG_H
